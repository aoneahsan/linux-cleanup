#!/usr/bin/env bash
# Stale node_modules finder — interactive only.
# Scans project search roots and lists node_modules whose project dir has not
# been modified in $DAYS+ days. User picks which to delete.

NM_SEARCH_ROOTS=(
  "$HOME/Documents/01-code/projects"
  "$HOME/Documents/01-code/02-apps"
  "$HOME/Documents/01-code"
)

run_stale_node_modules() {
  local days="${DAYS:-100}"
  ui_section "Stale node_modules (projects untouched ${days}+ days)"
  ui_warn "Interactive only — regenerable via 'yarn install'."

  local roots=() r
  for r in "${NM_SEARCH_ROOTS[@]}"; do
    [[ -d "$r" ]] && roots+=("$r")
  done
  if (( ${#roots[@]} == 0 )); then
    ui_info "No project roots found (looked in: ${NM_SEARCH_ROOTS[*]})"
    return
  fi
  ui_info "Searching: ${roots[*]}"

  local nm_paths=() nm
  while IFS= read -r nm; do
    [[ -n "$nm" ]] && nm_paths+=("$nm")
  done < <(
    find "${roots[@]}" -name node_modules -type d -prune -print 2>/dev/null \
      | awk '!seen[$0]++'
  )
  if (( ${#nm_paths[@]} == 0 )); then
    ui_info "No node_modules found."
    return
  fi

  local stale=() parent age
  for nm in "${nm_paths[@]}"; do
    parent="$(dirname "$nm")"
    age="$(dir_age_days "$parent")"
    if [[ "$age" =~ ^[0-9]+$ ]] && (( age >= days )); then
      stale+=("$nm")
    fi
  done

  if (( ${#stale[@]} == 0 )); then
    ui_info "No node_modules in projects ${days}+ days stale."
    return
  fi

  # Sort by size descending for visibility
  local sized=()
  while IFS= read -r line; do sized+=("$line"); done < <(
    for nm in "${stale[@]}"; do
      printf '%s\t%s\n' "$(dir_bytes "$nm")" "$nm"
    done | sort -t$'\t' -k1,1 -nr
  )

  printf "${C_BLD}%4s %10s %8s  %s${C_RST}\n" "#" "SIZE" "AGE-d" "PROJECT"
  local i=1 ranked=() bytes path_part age_part
  for line in "${sized[@]}"; do
    bytes="${line%%$'\t'*}"
    path_part="${line#*$'\t'}"
    age_part="$(dir_age_days "$(dirname "$path_part")")"
    printf "%4d %10s %8s  %s\n" "$i" "$(bytes_pretty "$bytes" | xargs)" "$age_part" "$(dirname "$path_part")"
    ranked+=("$path_part")
    ((i++))
    (( i > 200 )) && { ui_warn "(truncated to 200 entries)"; break; }
  done

  printf '\n%s\n' "Choose action:"
  printf '  %s\n' "a   delete ALL listed (regenerable via 'yarn install')"
  printf '  %s\n' "c   enter comma/range indexes (e.g. 1,3,5-7)"
  printf '  %s\n' "s   skip"
  local mode
  read -rp "> " mode

  delete_picks() {
    local -a picks=("$@")
    local total=0 b nm
    if ! ui_confirm "Confirm delete ${#picks[@]} node_modules?" n; then
      ui_info "aborted"; return
    fi
    for nm in "${picks[@]}"; do
      [[ -z "$nm" ]] && continue
      b=$(dir_bytes "$nm")
      if safe_rm "$nm"; then
        total=$(( total + b ))
        ui_ok "deleted $nm"
      fi
    done
    ui_ok "Total freed: $(bytes_pretty "$total")"
  }

  case "$mode" in
    a) delete_picks "${ranked[@]}" ;;
    c)
      local idxs picks=() parts p k
      read -rp "Indexes: " idxs
      local IFS=','; parts=($idxs); unset IFS
      for p in "${parts[@]}"; do
        p="${p// /}"
        if [[ "$p" =~ ^([0-9]+)-([0-9]+)$ ]]; then
          for ((k=${BASH_REMATCH[1]}; k<=${BASH_REMATCH[2]}; k++)); do
            picks+=("${ranked[$((k-1))]:-}")
          done
        elif [[ "$p" =~ ^[0-9]+$ ]]; then
          picks+=("${ranked[$((p-1))]:-}")
        fi
      done
      delete_picks "${picks[@]}"
      ;;
    *) ui_info "skipped" ;;
  esac
}
